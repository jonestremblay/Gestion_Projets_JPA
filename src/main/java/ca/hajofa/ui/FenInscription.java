    private boolean verifierChampsInscription() {
        //attribut
        boolean flag = true;
        final String emailRegex = "^(.+)@(.+)$";
        //verification des champs
        if (txtNom.getText().isEmpty() || txtPrenom.getText().isEmpty() || txtEmail.getText().isEmpty()
                || txtMdp1.getText().equals("") || txtMdp2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vous devez remplir tous les champs!",
                    "Invalide", JOptionPane.ERROR_MESSAGE);
            flag = false;
        } else if (!(txtMdp1.getText().equals(txtMdp2.getText()))) {
            JOptionPane.showMessageDialog(null, "Les mots de passes ne sont pas pareils!",
                    "Invalide", JOptionPane.ERROR_MESSAGE);
            flag = false;
        }else if((!(txtEmail.getText().matches("^(.+)@(.+)$"))) || (!(txtEmail.getText().contains(".")))){
            JOptionPane.showMessageDialog(null, "Entrez un courrier valide!",
                    "Invalide", JOptionPane.ERROR_MESSAGE);
            flag=false;
        }
        return flag;
    }
    
