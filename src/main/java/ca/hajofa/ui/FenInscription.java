    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        //attribut
        String courriel = txtEmail.getText();  
        //verification des champs
        if (verifierChampsInscription()) {   
            if (rBtnEtudiant.isSelected()) {
                //creer objet etudiant 
                Etudiants etudiant = new Etudiants (txtNom.getText(),txtPrenom.getText(),txtEmail.getText(),txtMdp2.getText()); 
                if(EtudiantsServices.createEtudiant(etudiant)){
                   JOptionPane.showMessageDialog(null, "Étudiant a été crée!",
                        "Valide", JOptionPane.INFORMATION_MESSAGE);
                   //retour a la page de connexion
                   deconnexion();
                }   
            } else if (rBtnEnseignant.isSelected()) {
                //creer objet etudiant 
                Enseignants prof = new Enseignants (txtNom.getText(),txtPrenom.getText(),txtEmail.getText(),txtMdp2.getText());
                if(EnseignantsServices.createEnseignant(prof)){
                   JOptionPane.showMessageDialog(null, "Enseignant a été crée!",
                        "Valide", JOptionPane.INFORMATION_MESSAGE);
                   //retour a la page de connexion
                   deconnexion();
                }
            }
        }
    }//GEN-LAST:event_btnEnregistrerActionPerformed

